function calculadora(numero1, numero2){
    console.log("Suma: "+(numero1+numero2));
    console.log("Resta: "+(numero1-numero2));
    console.log("multiplicasión: "+(numero1*numero2));
    console.log("División: "+(numero1/numero2));
}

calculadora(4,7);

calculadora(4,8);

calculadora(864,67);